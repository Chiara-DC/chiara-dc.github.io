function filterImages(images, filter) {
  let filteredimages = [];
  for (let i = 0; i < images.length; i++) {
    try {
      if (images[i].tags.includes(filter) === true) {
        filteredimages = [...filteredimages, images[i]];
      }
    } catch (err) {}
  }
  return filteredimages;
}

